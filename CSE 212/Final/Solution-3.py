print("Inorder traversal:")
def inorder_traversal(node):
    if node:
        inorder_traversal(node.left)
        print(node.key)
        inorder_traversal(node.right)

inorder_traversal(bst.root)

print("\nDelete 20:")
bst.delete(20)
inorder_traversal(bst.root)

